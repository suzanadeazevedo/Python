frase =  '   Curso em Video Python'

#transformação

#trocar palavras
print(frase.replace('Python', 'Android'))

#tornar Maisculas e tornar minusculos
print(frase.upper())
print(frase.lower())

# Só primeira letra em Maiusculo
print(frase.capitalize())

#Primeiras letras em maisculas
print(frase.title())

#remover espaços excedentes no inicio e final
print(frase.strip())

#remove espaço excedente a direita
print(frase.rstrip())

#remove espaço excedente a esquerda
print(frase.lstrip())
