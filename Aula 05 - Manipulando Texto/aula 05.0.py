frase =  'Curso em Video Python'

#fatiamento
print(frase[9])

# (Ultimo número não é pego)
print(frase[9:21])

#saltando de 2 em 2
print(frase[9:21:2])

#inicio até numero
print(frase[:5])

#inicio até final não determinado
print(frase[15:])

#inicio até final  indeterminado saltando caracteres
print(frase[9::3])